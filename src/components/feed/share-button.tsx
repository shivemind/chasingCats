'use client';

import { useState, useEffect } from 'react';
import { Share2, X, Check, Link2 } from 'lucide-react';
import { cn } from '@/lib/utils';

type ShareButtonProps = {
  postId: string;
  postContent: string;
};

export function ShareButton({ postId, postContent }: ShareButtonProps) {
  const [isOpen, setIsOpen] = useState(false);
  const [copied, setCopied] = useState(false);
  const [canNativeShare, setCanNativeShare] = useState(false);
  const [shareUrl, setShareUrl] = useState('');

  // Set up client-side values after mount to avoid hydration mismatch
  useEffect(() => {
    setShareUrl(`${window.location.origin}/feed?post=${postId}`);
    if ('share' in navigator) {
      setCanNativeShare(true);
    }
  }, [postId]);
  
  const shareText = postContent.slice(0, 100) + (postContent.length > 100 ? '...' : '');

  const handleNativeShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: 'Check out this post on Chasing Cats',
          text: shareText,
          url: shareUrl,
        });
        setIsOpen(false);
      } catch (err) {
        // User cancelled or error
      }
    }
  };

  const handleCopyLink = async () => {
    try {
      await navigator.clipboard.writeText(shareUrl);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      console.error('Failed to copy:', err);
    }
  };

  const shareToTwitter = () => {
    const url = `https://twitter.com/intent/tweet?text=${encodeURIComponent(shareText)}&url=${encodeURIComponent(shareUrl)}`;
    window.open(url, '_blank', 'width=550,height=420');
    setIsOpen(false);
  };

  const shareToFacebook = () => {
    const url = `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(shareUrl)}`;
    window.open(url, '_blank', 'width=550,height=420');
    setIsOpen(false);
  };

  const shareToWhatsApp = () => {
    const url = `https://wa.me/?text=${encodeURIComponent(shareText + ' ' + shareUrl)}`;
    window.open(url, '_blank');
    setIsOpen(false);
  };

  return (
    <div className="relative">
      <button
        onClick={() => setIsOpen(!isOpen)}
        className={cn(
          'flex items-center gap-1 sm:gap-1.5 rounded-full px-2.5 sm:px-3 py-1.5 text-xs sm:text-sm font-medium transition-all active:scale-95',
          isOpen
            ? 'bg-emerald-500/30 text-emerald-300'
            : 'bg-emerald-800/50 text-emerald-300/70 hover:bg-emerald-700/50 hover:text-emerald-200'
        )}
        title="Share post"
      >
        <Share2 className="h-3.5 w-3.5 sm:h-4 sm:w-4" />
        <span className="hidden sm:inline">Share</span>
      </button>

      {/* Share dropdown */}
      {isOpen && (
        <>
          {/* Backdrop */}
          <div 
            className="fixed inset-0 z-40" 
            onClick={() => setIsOpen(false)}
          />
          
          {/* Dropdown menu */}
          <div className="absolute right-0 top-full mt-2 z-50 w-48 rounded-xl border border-emerald-500/30 bg-emerald-950 p-2 shadow-lg">
            <div className="flex items-center justify-between mb-2 px-2 pb-2 border-b border-emerald-500/20">
              <span className="text-xs font-semibold text-emerald-200">Share to</span>
              <button 
                onClick={() => setIsOpen(false)}
                className="text-emerald-400/50 hover:text-emerald-300"
              >
                <X className="h-4 w-4" />
              </button>
            </div>

            {/* Native share (mobile) */}
            {canNativeShare && (
              <button
                onClick={handleNativeShare}
                className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm text-emerald-200 hover:bg-emerald-800/50 transition-colors"
              >
                <Share2 className="h-4 w-4" />
                Share...
              </button>
            )}

            {/* Twitter/X */}
            <button
              onClick={shareToTwitter}
              className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm text-emerald-200 hover:bg-emerald-800/50 transition-colors"
            >
              <svg className="h-4 w-4" viewBox="0 0 24 24" fill="currentColor">
                <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
              </svg>
              X (Twitter)
            </button>

            {/* Facebook */}
            <button
              onClick={shareToFacebook}
              className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm text-emerald-200 hover:bg-emerald-800/50 transition-colors"
            >
              <svg className="h-4 w-4" viewBox="0 0 24 24" fill="currentColor">
                <path d="M24 12.073c0-6.627-5.373-12-12-12s-12 5.373-12 12c0 5.99 4.388 10.954 10.125 11.854v-8.385H7.078v-3.47h3.047V9.43c0-3.007 1.792-4.669 4.533-4.669 1.312 0 2.686.235 2.686.235v2.953H15.83c-1.491 0-1.956.925-1.956 1.874v2.25h3.328l-.532 3.47h-2.796v8.385C19.612 23.027 24 18.062 24 12.073z" />
              </svg>
              Facebook
            </button>

            {/* WhatsApp */}
            <button
              onClick={shareToWhatsApp}
              className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm text-emerald-200 hover:bg-emerald-800/50 transition-colors"
            >
              <svg className="h-4 w-4" viewBox="0 0 24 24" fill="currentColor">
                <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
              </svg>
              WhatsApp
            </button>

            {/* Copy link */}
            <button
              onClick={handleCopyLink}
              className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm text-emerald-200 hover:bg-emerald-800/50 transition-colors"
            >
              {copied ? (
                <>
                  <Check className="h-4 w-4 text-emerald-400" />
                  <span className="text-emerald-400">Copied!</span>
                </>
              ) : (
                <>
                  <Link2 className="h-4 w-4" />
                  Copy link
                </>
              )}
            </button>
          </div>
        </>
      )}
    </div>
  );
}
